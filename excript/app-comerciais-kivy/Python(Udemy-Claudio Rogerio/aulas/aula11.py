#Operadores coparativos


numero1 = input("Digite um número: ")
numero1 = int(numero1)

numero2 = input("Digite um segundo numero:")
numero2 = int(numero2)

if(numero1==numero2): #operador de igualdade
    print("O numero %d é igual a %d. "%(numero1,numero2))
if(numero1!=numero2): #operador de difenreça
    print("O numero %d e diferente %d "%(numero1,numero2))
if(numero1<numero2): #operador menor que
    print("O numero %d e menor que %d "%(numero1,numero2))
if(numero1>numero2): #operador maior que
    print("O numero %d é maior que %d "%(numero1,numero2))
if(numero1>=numero2):#operador maior igaul que
    print("O numero %d e maior ou igual a %d "%(numero1,numero2))
if(numero1<=numero2):#operador menor igual que
    print("O numero %d a menor ou igual a %d "%(numero1,numero2))





